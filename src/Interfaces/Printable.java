package Interfaces;

/**
 * This interface is to be implemented to execute its printInfo() method,
 * which will be used to print information about the created instance of File class.
 *
 * @author Shake Gharibyan
 * @version 1.0
 * @since 2021-01-04
*/
public interface Printable {
    /**
     * This method is to be used to print all information of the instance of the class,
     * which implements Printable interface.
     *
     * @since 2021-01-04
     */

    void printInfo();
}
