package Interfaces;

/**
 * This interface is to be implemented to execute its toString() method,
 * which will be used to convert the information of the instance of the
 * class, which implements Stringify interface, to string.
 *
 * @author Shake Gharibyan
 * @version 1.0
 * @since 2021-01-04
 */
public interface Stringify {
    /**
     * This method is to be used to convert the information of the instance
     * of the class, which implements Stringify interface, to string.
     *
     * @version 1.0
     * @since 2021-01-04
     */

    String toString();
}
